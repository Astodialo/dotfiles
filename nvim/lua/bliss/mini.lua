-- Usage guideline
-- if we have `w[x]ord` where `[x]` denote cursor position, press `saiw` and then `'` to transform this into `'word'`, and selecting this word and then pressing `sa` and then `'` will also do the same thing. Similarly, their is `sd` to delete and `sr` to replace. Their demo (https://github.com/echasnovski/mini.nvim/blob/main/readmes/mini-surround.md#demo) explains this.
