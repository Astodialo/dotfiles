-- this code was written so that "super tab", i.e., using same tab key to move accross different fields of snippets, works better. 
local check_backspace = function()
  local col = vim.fn.col "." - 1
  return col == 0 or vim.fn.getline("."):sub(col, col):match "%s"
end

